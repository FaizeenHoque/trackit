const WATER_COLORS = ["#4A90D9", "#3D7FC4", "#5BA0E8", "#4888CC"];
const LAND_COLORS = ["#5B9E4B", "#4A8E3A", "#6BAF5C", "#3D7A2E"];
const ICE_COLORS = ["#E8E8F0", "#D8D8E4", "#F0F0F8"];

function getLandAt(row: number, col: number): number {
  const r = ((row % MAP_ROWS) + MAP_ROWS) % MAP_ROWS;
  const c = ((col % MAP_COLS) + MAP_COLS) % MAP_COLS;
  return LAND[r * MAP_COLS + c];
}

function hashNoise(x: number, y: number): number {
  let h = (x * 374761393 + y * 668265263 + 1274126177) & 0xffffffff;
  h = ((h ^ (h >> 13)) * 1103515245 + 12345) & 0xffffffff;
  return (h & 0xff) / 255;
}

export default function PixelEarth() {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const mouseRef = useRef({ x: 0.5, y: 0.5 });
  const rotRef = useRef({ lonOffset: 0, latOffset: 0 });
  const rafRef = useRef(0);

  const draw = useCallback(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    const W = canvas.width;
    const H = canvas.height;
    const cx = W / 2;
    const cy = H / 2;
    const R = Math.min(W, H) * 0.38;

    // Smooth rotation toward mouse
    const targetLon = (mouseRef.current.x - 0.5) * 360;
    const targetLat = (mouseRef.current.y - 0.5) * -120;
    rotRef.current.lonOffset += (targetLon - rotRef.current.lonOffset) * 0.04;
    rotRef.current.latOffset += (targetLat - rotRef.current.latOffset) * 0.04;

    const lonOff = (rotRef.current.lonOffset / 360) * MAP_COLS;
    const latOff = (rotRef.current.latOffset / 180) * MAP_ROWS;

    ctx.clearRect(0, 0, W, H);

    // Draw pixel sphere
    for (let py = cy - R; py < cy + R; py += PX) {
      for (let px = cx - R; px < cx + R; px += PX) {
        const dx = (px + PX / 2 - cx) / R;
        const dy = (py + PX / 2 - cy) / R;
        const d2 = dx * dx + dy * dy;
        if (d2 > 1) continue;

        const dz = Math.sqrt(1 - d2);

        // Spherical coords
        const lat = Math.asin(dy);
        const lon = Math.atan2(dx, dz);

        // Map to grid
        const mapRow = Math.floor(((lat / Math.PI + 0.5) * MAP_ROWS) - latOff);
        const mapCol = Math.floor(((lon / Math.PI + 0.5) * MAP_COLS) + lonOff);

        const isLand = getLandAt(mapRow, mapCol);
        const noise = hashNoise(Math.floor(px / PX), Math.floor(py / PX));
        const absRow = ((mapRow % MAP_ROWS) + MAP_ROWS) % MAP_ROWS;
        const isIce = absRow < 2 || absRow > 33;

        let color: string;
        if (isIce && !isLand) {
          color = ICE_COLORS[Math.floor(noise * ICE_COLORS.length)];
        } else if (isLand) {
          color = LAND_COLORS[Math.floor(noise * LAND_COLORS.length)];
        } else {
          color = WATER_COLORS[Math.floor(noise * WATER_COLORS.length)];
        }

        // Simple shading
        const shade = 0.55 + 0.45 * (dz * 0.7 + (1 - dy) * 0.3);
        ctx.fillStyle = color;
        ctx.globalAlpha = shade;
        ctx.fillRect(Math.floor(px / PX) * PX, Math.floor(py / PX) * PX, PX, PX);
      }
    }

    // Atmosphere glow
    ctx.globalAlpha = 1;
    const grad = ctx.createRadialGradient(cx, cy, R * 0.95, cx, cy, R * 1.15);
    grad.addColorStop(0, "hsla(200, 60%, 70%, 0.15)");
    grad.addColorStop(0.5, "hsla(200, 60%, 70%, 0.06)");
    grad.addColorStop(1, "transparent");
    ctx.fillStyle = grad;
    ctx.fillRect(0, 0, W, H);

    rafRef.current = requestAnimationFrame(draw);
  }, []);

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;

    const resize = () => {
      canvas.width = window.innerWidth;
      canvas.height = window.innerHeight;
    };
    resize();
    window.addEventListener("resize", resize);

    const onMove = (e: MouseEvent) => {
      mouseRef.current = { x: e.clientX / window.innerWidth, y: e.clientY / window.innerHeight };
    };
    window.addEventListener("mousemove", onMove);

    rafRef.current = requestAnimationFrame(draw);

    return () => {
      window.removeEventListener("resize", resize);
      window.removeEventListener("mousemove", onMove);
      cancelAnimationFrame(rafRef.current);
    };
  }, [draw]);

  return (
    <canvas
      ref={canvasRef}
      className="fixed inset-0 -z-10"
      style={{ background: "hsl(45 30% 95%)" }}
    />
  );
}